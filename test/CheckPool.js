// Khởi tạo contract ZetaFactory
const factoryContract = new ethers.Contract(FACTORY_ADDRESS, FACTORY_ABI, provider);

// Hàm kiểm tra pool của hai token
async function checkPool(tokenA, tokenB) {
  try {
    const pairAddress = await factoryContract.getPair(tokenA, tokenB);

    if (pairAddress !== ethers.constants.AddressZero) {
      console.log(`✅ Pool tồn tại tại địa chỉ: ${pairAddress}`);
    } else {
      console.log("❌ Pool chưa tồn tại.");
    }

    const tokenAContract = new ethers.Contract(tokenA, WZETA_ABI, provider);
    const tokenBContract = new ethers.Contract(tokenB, ZTW_ABI, provider);

    // Lấy số dư của từng token trong pool
    const balanceA = await tokenAContract.balanceOf(pairAddress);
    const balanceB = await tokenBContract.balanceOf(pairAddress);

    console.log(`📌 Số lượng ${tokenA} trong pool: ${ethers.utils.formatUnits(balanceA, 18)}`);
    console.log(`📌 Số lượng ${tokenB} trong pool: ${ethers.utils.formatUnits(balanceB, 18)}`);
  } catch (error) {
    console.error("Lỗi khi kiểm tra pool:", error);
  }
}


// Gọi hàm kiểm tra (Thay tokenA và tokenB bằng địa chỉ thực)
const tokenA = "0x5F0b1a82749cb4E2278EC87F8BF6B618dC71a8bf"; // Thay bằng địa chỉ token A
const tokenB = "0x4758847f1ef54dbe32aD24d05B0c673F832aDc05"; // Thay bằng địa chỉ token B

checkPool(tokenA, tokenB);
