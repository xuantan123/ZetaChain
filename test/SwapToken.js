async function approveTokens() {
    console.log("\n🛠 Kiểm tra & cấp quyền cho Router...");

    const amount = ethers.constants.MaxUint256; // Cấp quyền vô hạn
    const userAddress = await signer.getAddress();

    // Kiểm tra và approve TOKEN_A (WZETA)
    let allowanceA = await tokenContractA.allowance(userAddress, ROUTER_ADDRESS);
    console.log(`🔎 Quyền hạn hiện tại của WZETA: ${ethers.utils.formatEther(allowanceA)}`);

    if (allowanceA.lt(amount)) {
        console.log("🔄 Đang approve WZETA...");
        try {
            let txA = await tokenContractA.approve(ROUTER_ADDRESS, amount);
            console.log(`📌 Giao dịch approve WZETA gửi đi: ${txA.hash}`);
            await txA.wait();
            console.log("✅ WZETA đã được cấp quyền thành công!");
        } catch (error) {
            console.error("❌ Lỗi khi approve WZETA:", error);
        }
    } else {
        console.log("✅ WZETA đã được approve sẵn!");
    }

    // Kiểm tra và approve TOKEN_B (ZTW)
    let allowanceB = await tokenContractB.allowance(userAddress, ROUTER_ADDRESS);
    console.log(`🔎 Quyền hạn hiện tại của ZTW: ${ethers.utils.formatEther(allowanceB)}`);

    if (allowanceB.lt(amount)) {
        console.log("🔄 Đang approve ZTW...");
        try {
            let txB = await tokenContractB.approve(ROUTER_ADDRESS, amount);
            console.log(`📌 Giao dịch approve ZTW gửi đi: ${txB.hash}`);
            await txB.wait();
            console.log("✅ ZTW đã được cấp quyền thành công!");
        } catch (error) {
            console.error("❌ Lỗi khi approve ZTW:", error);
        }
    } else {
        console.log("✅ ZTW đã được approve sẵn!");
    }
}
async function checkLiquidity() {
    console.log("\n🔍 Kiểm tra thanh khoản của cặp WZETA-ZTW...");
    const pairAddress = await factoryContract.getPair(TOKEN_A, TOKEN_B);
    
    if (pairAddress === ethers.constants.AddressZero) {
        console.log("❌ Cặp thanh khoản chưa tồn tại!");
        return false;
    }

    const pairContract = new ethers.Contract(pairAddress, PairABI, signer);
    const reserves = await pairContract.getReserves();

    if (reserves[0].eq(0) || reserves[1].eq(0)) {
        console.log("❌ Không có thanh khoản trong cặp!");
        return false;
    }

    console.log("✅ Cặp thanh khoản có đủ thanh khoản!");
    return true;
}
async function checkBalance() {
    const balance = await tokenContractA.balanceOf(await signer.getAddress());
    console.log(`🔎 Số dư WZETA: ${ethers.utils.formatEther(balance)} WZETA`);
    
    if (balance.lt(ethers.utils.parseEther("0.1"))) {
        console.log("❌ Không đủ WZETA để swap!");
        return false;
    }

    return true;
}
async function SwapToken() {
    console.log(`\n🔄 Bắt đầu quá trình swap ETH sang ZTW...\n`);

    try {
        const userAddress = await signer.getAddress();
        console.log(`📌 Địa chỉ ví: ${userAddress}`);

        if (!(await checkBalance()) || !(await checkLiquidity())) {
            console.log("⛔ Swap bị hủy do không đủ điều kiện.");
            return false;
        }

        const amountOut = ethers.utils.parseEther("10000"); // Số lượng token muốn nhận
        console.log(`🔢 Số ZTW mong muốn nhận: ${ethers.utils.formatEther(amountOut)} ZTW`);

        // Lấy địa chỉ WZETA từ contract
        const contractWZETA = await routerContract.WZETA();
        if (TOKEN_A !== contractWZETA) {
            console.error("❌ TOKEN_A không phải là WZETA!");
            return false;
        }

        // Kiểm tra đường dẫn swap hợp lệ
        const path = [TOKEN_A, TOKEN_B];
        if (!ethers.utils.isAddress(path[0]) || !ethers.utils.isAddress(path[1])) {
            console.error("❌ Đường path không hợp lệ!");
            return false;
        }

        console.log("🔍 Đang tính toán số lượng WZETA cần thiết...");
        const amounts = await routerContract.getAmountsIn(amountOut, path);
        const amountIn = amounts[0];
        const deadline = Math.floor(Date.now() / 1000) + 60 * 20;

        console.log(`📊 Thông tin giao dịch:`)
        console.log(`- 🪙 Số WZETA cần gửi: ${ethers.utils.formatEther(amountIn)} WZETA`);
        console.log(`- 🎯 Số ZTW nhận: ${ethers.utils.formatEther(amountOut)} ZTW`);
        console.log(`- ⏳ Hạn giao dịch: ${new Date(deadline * 1000).toLocaleString()}\n`);

        // Wrap ETH thành WZETA nếu cần
        console.log("🔄 Đang wrap ETH thành WZETA...");
        const WZETAContract = new ethers.Contract(contractWZETA, WZETA_ABI, signer);
        const wrapTx = await WZETAContract.deposit({ value: amountIn });
        await wrapTx.wait();
        console.log(`✅ Đã wrap ${ethers.utils.formatEther(amountIn)} ETH thành WZETA.`);

        console.log("🚀 Gửi giao dịch swap...");
        const tx = await routerContract.callStatic.swapETHForExactTokens(
            amountOut,
            path,
            userAddress,
            deadline,
            {
                value: amountIn,
                gasLimit: ethers.utils.hexlify(8000000),
                gasPrice: ethers.utils.parseUnits("100", "gwei")
            }
        );

        console.log(`📌 Giao dịch gửi đi: ${tx.hash}`);
        console.log(`⏳ Đang chờ xác nhận trên blockchain...`);
        const receipt = await tx.wait();

        console.log(`✅ Swap thành công!`);
        console.log(`📍 Block Number: ${receipt.blockNumber}`);
        console.log(`🔗 Kiểm tra giao dịch: https://explorer.zetachain.com/tx/${tx.hash}`);

        return true;
    } catch (error) {
        console.error("❌ Swap thất bại!", error.message);

        if (error.error && error.error.message) {
            console.error("🔍 Lỗi từ blockchain:", error.error.message);
        }

        if (error.transaction) {
            console.error(`🔍 Giao dịch thất bại: https://explorer.zetachain.com/tx/${error.transaction.hash}`);
        }

        return false;
    }
}



